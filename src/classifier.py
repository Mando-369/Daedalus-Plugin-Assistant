def classify_plugin(name: str) -> dict:
    """
    Classify a plugin by name. Returns classification dict.
    First tries exact/prefix match in KNOWN_PLUGINS, then pattern matching.
    """
    name_lower = name.lower().strip()

    # 1. Try exact match first
    for key, data in KNOWN_PLUGINS.items():
        if name_lower == key.lower():
            return {**data, "classification_confidence": "high", "needs_review": 0}

    # 2. Try prefix match (handles versioned names like "uaudio_1176_rev_a")
    for key, data in KNOWN_PLUGINS.items():
        key_lower = key.lower()
        if name_lower.startswith(key_lower) or key_lower.startswith(name_lower):
            return {**data, "classification_confidence": "high", "needs_review": 0}

    # 3. Try partial match (e.g., "SSL Native Bus Compressor 2" matches "SSL Native Bus Compressor 2")
    for key, data in KNOWN_PLUGINS.items():
        key_lower = key.lower()
        # Check if key appears as a significant substring
        if len(key_lower) > 5 and (key_lower in name_lower or name_lower in key_lower):
            return {**data, "classification_confidence": "medium", "needs_review": 1}

    # 4. Pattern-based classification
    for pattern, category, plugin_type, confidence in NAME_PATTERNS:
        if re.search(pattern, name):
            return {
                "plugin_type": plugin_type,
                "category": category,
                "classification_confidence": confidence,
                "needs_review": 1,
            }

    # 5. Unknown
    return {
        "classification_confidence": "unclassified",
        "needs_review": 1,
    }


def classify_all(plugins: list[dict]) -> list[dict]:
    """Classify all scanned plugins. Returns enriched plugin list."""
    classified = 0
    pattern_matched = 0
    unclassified = 0

    for plugin in plugins:
        classification = classify_plugin(plugin["name"])
        # Don't overwrite scanner-provided developer with empty classifier result
        if plugin.get("developer") and not classification.get("developer"):
            classification.pop("developer", None)
        plugin.update(classification)

        conf = classification.get("classification_confidence", "unclassified")
        if conf == "high":
            classified += 1
        elif conf in ("medium", "low"):
            pattern_matched += 1
        else:
            unclassified += 1

    print(f"\n  Classification results:")
    print(f"    High confidence: {classified}")
    print(f"    Pattern-matched: {pattern_matched}")
    print(f"    Unclassified:    {unclassified}")
    print(f"    Total:           {len(plugins)}")

    return plugins


